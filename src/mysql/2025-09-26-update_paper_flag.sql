UPDATE PAPERS p
SET p.FLAG = 'imported'
WHERE p.FLAG != 'imported'
AND (
DATE(p.PUBLICATION_DATE) <= DATE(p.SUBMISSION_DATE)
OR YEAR(DATE(p.PUBLICATION_DATE)) < 2013
OR YEAR(SUBMISSION_DATE) < 2013
OR (DATE(SUBMISSION_DATE) > DATE(`WHEN`) AND STATUS = 16)
);

