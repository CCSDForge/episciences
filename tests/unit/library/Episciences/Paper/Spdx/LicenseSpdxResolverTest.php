<?php

namespace unit\library\Episciences\Paper\Spdx;


use Episciences\Paper\Spdx\LicenseProviderInterface;
use Episciences\Paper\Spdx\LicenseSpdxResolver;
use PHPUnit\Framework\TestCase;


class LicenseSpdxResolverTest extends TestCase
{
    private LicenseSpdxResolver $resolver;
    private ?LicenseProviderInterface $mockProvider;

    protected function setUp(): void
    {
        $this->mockProvider = $this->createMock(LicenseProviderInterface::class);

        $spdxIndex = [
                'mit' => 'MIT',
                'apache-2.0' => 'Apache-2.0',
                'apache-1.1' => 'Apache-1.1',
                'gpl-3.0' => 'GPL-3.0',
                'cc0-1.0' => 'CC0-1.0',
                'cc-by-4.0' => 'CC-BY-4.0',
                'cc-by-nc-4.0' => 'CC-BY-NC-4.0',
                'cc-by-nd-4.0' => 'CC-BY-ND-4.0',
                'cc-by-nc-nd-4.0' => 'CC-BY-NC-ND-4.0',
                'cc-by-sa-4.0' => 'CC-BY-SA-4.0',
                'cc-by-nc-sa-4.0' => 'CC-BY-NC-SA-4.0',
                'cc-by-nc-sa-3.0' => 'CC-BY-NC-SA-3.0',
                'etalab-2.0' => 'Etalab-2.0',
                'ofl-1.1' => 'OFL-1.1',
                'cc-pddc' => 'CC-PDDC',
                'bsd-3-clause' => 'BSD-3-Clause',
        ];
        // When resolve() calls $licenseProvider->loadSpdxCode(),
        // it will immediately receive the predefined $spdxIndex,
        // without making any network requests or reading a file from disk
        $this->mockProvider->method('loadSpdxCode')->willReturn($spdxIndex);
        $this->resolver = new LicenseSpdxResolver($this->mockProvider);
    }

    // data-driven test
    // Executed as many times as there are lines in the list generated by "urlToSpdxCodeProvider" function

    /**
     * @dataProvider urlToSpdxCodeProvider
     */
    public function testUrlToSpdxCode(string $url, string $expected): void
    {
        $result = LicenseSpdxResolver::urlToSpdxCode($url);
        $this->assertEquals($expected, $result);
    }

    public static function urlToSpdxCodeProvider(): array
    {
        return [
                ['https://spdx.org/licenses/MIT.html', 'MIT'],
                ['https://spdx.org/licenses/Apache-2.0.html', 'Apache-2.0'],
                ['http://not.spdx.url/example', ''],
                ['https://spdx.org/licenses/INVALID-License.html', 'INVALID-License'], // Official SPDX URL
                ['', ''],
        ];
    }

    /**
     * @covers ::resolve
     * @dataProvider resolveProvider
     */
    public function testResolve(mixed $input, string $expected): void
    {
        $result = $this->resolver->resolve($input);
        $this->assertEquals($expected, $result);
    }

    public static function resolveProvider(): array
    {
        return [
            // Direct case
                ['MIT', 'MIT'],
                ['Apache-2.0', 'Apache-2.0'],
                ['GPL-3.0', 'GPL-3.0'],


            // Official SPDX URL
                ['https://spdx.org/licenses/MIT.html', 'MIT'],
                ['https://spdx.org/licenses/Apache-2.0.html', 'Apache-2.0'],

            //  Creative Commons
                ['https://creativecommons.org/licenses/by/4.0/', 'CC-BY-4.0'],
                ['https://creativecommons.org/licenses/by-nc/4.0/', 'CC-BY-NC-4.0'],
                ['https://creativecommons.org/licenses/by-nc/4/', 'CC-BY-NC-4.0'], // version 4 to 4.0
                ['https://creativecommons.org/licenses/by-nc/', 'CC-BY-NC-4.0'], // without version
                ['https://creativecommons.org/licenses/by-nd/4.0/', 'CC-BY-ND-4.0'],
                ['https://creativecommons.org/licenses/by-sa/4.0/', 'CC-BY-SA-4.0'],
                ['https://creativecommons.org/licenses/by-nc-nd/4.0/', 'CC-BY-NC-ND-4.0'],
                ['https://creativecommons.org/licenses/by-nc-sa/4.0/', 'CC-BY-NC-SA-4.0'],
            // normalized order
                ['https://creativecommons.org/licenses/by-nd-nc/4.0/', 'CC-BY-NC-ND-4.0'],
                ['https://creativecommons.org/licenses/by-sa-nc/3.0/', 'CC-BY-NC-SA-3.0'],
            // CC0 and Public Domain
                ['https://creativecommons.org/publicdomain/zero/1.0/', 'CC0-1.0'],
                ['https://creativecommons.org/publicdomain/mark/1.0', 'CC-PDDC'],

            // Special mapping
                ['https://creativecommons.org/choose/mark', 'CC-PDDC'],
                ['https://scripts.sil.org/cms/scripts/page.php?item_id=ofl_web', 'OFL-1.1'],
                ['https://raw.githubusercontent.com/disic/politique-de-contribution-open-source/master/license', 'Etalab-2.0'],

            // Etalab via regex
                ['https://etalab.gouv.fr/licence-etalab-2.0', 'Etalab-2.0'],

            //  Normalize /open source.org
                ['https://opensource.org/licenses/mit', 'MIT'],

            //  GNU GPL via normalize
                ['https://www.gnu.org/licenses/gpl-3.0.en.html', 'GPL-3.0'],

        ];

    }

    /**
     * @dataProvider apacheLicenseProvider
     */
    public function testApacheLicenseUrl(string $url, string $expected): void
    {
        $result = $this->resolver->resolve($url);
        $this->assertEquals($expected, $result);
    }

    public static function apacheLicenseProvider(): array
    {
        return [
            // HTTPS with  www
                ['https://www.apache.org/licenses/LICENSE-2.0', 'Apache-2.0'],
            // HTTPS without www
                ['https://apache.org/licenses/LICENSE-2.0', 'Apache-2.0'],
            // HTTP converted to HTTPS using normalizeUrl
                ['http://www.apache.org/licenses/LICENSE-2.0', 'Apache-2.0'],
            // Trailing slash removed by normalizeUrl
                ['https://www.apache.org/licenses/LICENSE-2.0/', 'Apache-2.0'],
            // v1.1
                ['https://www.apache.org/licenses/LICENSE-1.1', 'Apache-1.1'],
                ['https://www.apache.org/licenses/LICENSE-2.0.html', 'Apache-2.0'],
                ['https://www.apache.org/licenses/LICENSE-2.0.txt', 'Apache-2.0'],

        ];
    }

    /**
     * @dataProvider authorizationProvider
     */
    public function testAuthorizationProvider(string $url, string $expected): void
    {
        $result = $this->resolver->resolve($url);
        $this->assertEquals($expected, $result);
    }

    public static function authorizationProvider(): array
    {
        return [
                ['https://arxiv.org/licenses/nonexclusive-distrib/1.0', LicenseSpdxResolver::NO_ASSERTION],
                ['arxiv.org/licenses/assumed-1991-2003', LicenseSpdxResolver::NO_ASSERTION],
                ['https://hal.archives-ouvertes.fr/licences/copyright', LicenseSpdxResolver::NO_ASSERTION],
                ['https://hal.science/licences/copyright', LicenseSpdxResolver::NO_ASSERTION],
                ['https://about.hal.science/hal-authorisation-v1', LicenseSpdxResolver::NO_ASSERTION]
        ];

    }

    /**
     * @covers ::isValid
     * @dataProvider isValidProvider
     */
    public function testIsValid(string $code, bool $expected): void
    {
        $result = $this->resolver->isValid($code);
        $this->assertEquals($expected, $result);
    }

    /**
     * @covers ::isValid
     * @dataProvider isStrictValidProvider
     */
    public function testStrictIsValid(string $code, bool $expected): void
    {
        $result = $this->resolver->isValid($code, true);
        $this->assertEquals($expected, $result);
    }

    public static function isValidProvider(): array
    {
        return [
                ['MIT', true],
                ['Apache-2.0', true],
                ['GPL-3.0', true],
                ['CC0-1.0', true],
                ['CC-BY-4.0', true], // The original value is in the index
                [LicenseSpdxResolver::NO_ASSERTION, false], // is not a valid license
                ['', false],
        ];
    }

    public static function isStrictValidProvider(): array
    {
        return [
                ['mit', false],
                ['APACHE-2.0', false],
                ['CC-BY-4.0', true],
                ['CC-by-4.0', false],
        ];
    }
    
}