/**
 * BiblioRef - Modern bibliographic references visualization
 * Handles fetching and displaying citations from external API
 */

/**
 * Service class for API calls
 */
class BiblioRefService {
    /**
     * Fetch citations from API
     * @param {string} apiUrl - Base API URL
     * @param {string} paperUrl - Paper URL to fetch citations for
     * @param {boolean} showAll - Whether to show all citations including non-accepted
     * @returns {Promise<Object>} API response data
     * @throws {Error} When API call fails
     */
    async fetchCitations(apiUrl, paperUrl, showAll = false) {
        if (!apiUrl || !paperUrl) {
            throw new Error('API URL and paper URL are required');
        }

        let url = `${apiUrl}/visualize-citations?url=${encodeURIComponent(paperUrl)}`;
        if (showAll) {
            url += '&all=1';
        }

        const response = await fetch(url);

        if (!response.ok) {
            const errorData = await response.json().catch(() => ({}));
            throw new Error(
                errorData.message || `HTTP error! status: ${response.status}`
            );
        }

        return response.json();
    }
}

/**
 * Parser class for citation data processing
 */
class BiblioRefParser {
    /**
     * DOI regex pattern (validates DOI format)
     */
    static DOI_REGEX = /^10\.\d{4,9}\/[-._;()/:A-Z0-9]+$/i;

    /**
     * Human-readable labels for PPS Solr status values.
     * Each entry: { label: string, color: string, desc: string }
     * color maps to Bootstrap 3 label variant (label-{color}).
     * Note: `desc` is intentionally kept in English — it is used as a tooltip and is too long to be a translation key.
     * Only `label` is passed through translate() at render time.
     */
    static STATUS_LABELS = {
        'Problematic': {
            label: "Problematic",
            color: "danger",
            desc:  "Shows signs of manipulation or automated generation: tortured phrases, paper mill indicators (identical structures, duplicated images, statistically improbable datasets), or irrelevant citations used to artificially inflate impact factors.",
        },
        'Genuine': {
            label: "Genuine",
            color: "success",
            desc:  "Follows expected methodological rigor: standard scientific terminology, verifiable and consistent data and methodology, no traces of low-quality paraphrasing software.",
        },
    };

    /**
     * Human-readable labels and descriptions for PPS Solr detectors.
     * Each entry: { label: string, color: string, desc: string }
     * color maps to Bootstrap 3 label variant (label-{color}).
     * Note: `desc` is intentionally kept in English — it is used as a tooltip and is too long to be a translation key.
     * Only `label` is passed through translate() at render time.
     */
    static DETECTOR_LABELS = {
        'annulled':               { label: "Retracted",                   color: "info", desc: "Article officially retracted or subject to an editorial notice of concern." },
        'expression-of-concern':  { label: "Expression of Concern",       color: "info", desc: "Serious scientific integrity anomalies flagged by experts." },
        'clayFeet':               { label: "Feet of Clay",                color: "info", desc: "Relies on already-retracted works, undermining its own conclusions." },
        'tortured':               { label: "Tortured Phrases",            color: "info", desc: "Low-quality automated paraphrasing detected (e.g. \"artificial conscience\" instead of \"artificial intelligence\")." },
        'suspect':                { label: "Suspect",                     color: "info", desc: "Unusual characteristics (e.g. disposable email addresses) requiring in-depth human inspection." },
        'scigen':                 { label: "SCIgen-generated",            color: "info", desc: "Computer science article automatically generated by the SCIgen program — grammatically correct but meaningless." },
        'mathgen':                { label: "Mathgen-generated",           color: "info", desc: "Mathematical formulas and text randomly generated by the Mathgen program." },
        'citejacked':             { label: "Citejacked",                  color: "info", desc: "Unjustified citations used to artificially inflate the impact metrics of a researcher or journal." },
        'Seek&Blastn':            { label: "Suspect Biological Sequences",color: "info", desc: "DNA/RNA sequences described in the article do not match real genomic databases." },
        'problematic-cell-lines': { label: "Problematic Cell Lines",      color: "info", desc: "Use of contaminated or misidentified cell lines, invalidating the experimental results." },
        'deindexed':              { label: "Deindexed",                   color: "info", desc: "Article removed from scientific indexes." },
        'journal-cases':          { label: "Problematic Journal",         color: "info", desc: "Article published in a journal flagged for integrity issues." },
        'sbir':                   { label: "SBIR flagged",                color: "info", desc: "Article flagged by the Systematic Bibliometric Integrity Review." },
    };

    /**
     * Parse and format a single citation
     * @param {Object} citation - Raw citation object
     * @param {boolean} isAuthorizedToSeeAcc - Whether user can see acceptance status
     * @returns {Object|null} Formatted citation or null if invalid
     */
    static parseCitation(citation, isAuthorizedToSeeAcc = false) {
        if (!citation || !citation.ref) {
            return null;
        }

        try {
            // Handle both old API (ref is double-encoded JSON string) and new API (ref is already object)
            const parsedRef = typeof citation.ref === 'string' ? JSON.parse(citation.ref) : citation.ref;

            const detectors  = Array.isArray(parsedRef.detectors)  ? parsedRef.detectors  : [];
            const status     = Array.isArray(parsedRef.status)     ? parsedRef.status     : [];
            // Solr may return a single string instead of an array when there is only one value
            const rawPubpeer = parsedRef.pubpeerurl;
            const pubpeerurl = Array.isArray(rawPubpeer) ? rawPubpeer
                : (typeof rawPubpeer === 'string' && rawPubpeer) ? [rawPubpeer]
                : [];
            const isSuspect  = detectors.length > 0 || status.includes('Problematic') || pubpeerurl.length > 0;
            const isGenuine  = !isSuspect && status.includes('Genuine');

            return {
                rawReference: parsedRef.raw_reference,
                doi:          parsedRef.doi,
                isAccepted:      citation.isAccepted === 1,
                showAccepted:    isAuthorizedToSeeAcc && citation.isAccepted === 1,
                showNotAccepted: isAuthorizedToSeeAcc && citation.isAccepted !== 1,
                detectors,
                status,
                pubpeerurl,
                isSuspect,
                isGenuine,
            };
        } catch (error) {
            console.error('Failed to parse citation reference:', error);
            return null;
        }
    }

    /**
     * Validate and format DOI
     * @param {string} doi - DOI to validate
     * @returns {Object|null} Object with url and display text, or null if invalid
     */
    static formatDoi(doi) {
        if (!doi) {
            return null;
        }

        const match = doi.match(this.DOI_REGEX);
        const url = match ? `https://doi.org/${match[0]}` : doi;

        return {
            url,
            text: doi,
        };
    }
}

/**
 * Renderer class for DOM manipulation
 */
class BiblioRefRenderer {
    /**
     * Create a new renderer
     * @param {HTMLElement} container - Container element for citations list
     */
    constructor(container) {
        if (!container) {
            throw new Error('Container element is required');
        }
        this.container = container;
    }

    /**
     * Create a Font Awesome icon element
     * @param {string} classes - FA class string (e.g. "fa-solid fa-check")
     * @returns {HTMLElement}
     */
    _makeIcon(classes) {
        const icon = document.createElement('i');
        icon.className = classes;
        icon.setAttribute('aria-hidden', 'true');
        return icon;
    }

    /**
     * Render a single citation as list item
     * @param {Object} citation - Formatted citation object
     * @returns {HTMLLIElement} List item element
     */
    renderCitation(citation) {
        const li = document.createElement('li');

        if (citation.isSuspect) {
            li.classList.add('biblio-ref-item--suspect');
        }

        // Add suspect / accepted / not-accepted icon
        if (citation.isSuspect) {
            const icon = this._makeIcon('fa-solid fa-square-xmark');
            icon.style.color = '#c0392b';
            li.appendChild(icon);
            li.appendChild(document.createTextNode(' '));
        } else if (citation.showAccepted) {
            const icon = this._makeIcon('fa-solid fa-square-check');
            icon.style.color = '#009527';
            li.appendChild(icon);
            li.appendChild(document.createTextNode(' '));
        } else if (citation.showNotAccepted) {
            const icon = this._makeIcon('fa-solid fa-square');
            icon.style.color = '#aaa';
            li.appendChild(icon);
            li.appendChild(document.createTextNode(' '));
        }

        // Add raw reference text
        li.appendChild(document.createTextNode(citation.rawReference || ''));

        // Add DOI link if present (only safe http/https URLs)
        if (citation.doi) {
            const formattedDoi = BiblioRefParser.formatDoi(citation.doi);
            if (formattedDoi && /^https?:\/\/[^\s<>"]+$/.test(formattedDoi.url)) {
                const a = document.createElement('a');
                a.href = formattedDoi.url;
                a.rel = 'noopener';
                a.target = '_blank';
                a.textContent = formattedDoi.text;
                li.appendChild(document.createTextNode(' '));
                li.appendChild(a);
            }
        }

        // Append colored badges for suspect signals, or genuine badge
        if (citation.isSuspect) {
            li.appendChild(this.renderSuspectBadges(citation));
        } else if (citation.isGenuine) {
            li.appendChild(document.createTextNode(' '));
            li.appendChild(this.renderGenuineBadge());
        }

        return li;
    }

    /**
     * Build a single Bootstrap label badge for a PPS signal.
     * @param {string} label - Visible text (translation key)
     * @param {string} color - Bootstrap label variant (danger, warning, info, primary, default…)
     * @param {string} [desc] - Tooltip description
     * @param {string} [iconClasses] - Optional FA icon classes (e.g. "fa-solid fa-triangle-exclamation")
     * @returns {HTMLElement}
     */
    _makeBadge(label, color, desc, iconClasses) {
        const badge = document.createElement('span');
        badge.className = `label label-${color || 'default'}`;
        if (desc) {
            badge.title = desc;
        }
        if (iconClasses) {
            badge.appendChild(this._makeIcon(iconClasses));
            badge.appendChild(document.createTextNode(' '));
        }
        // translate() is a global provided by translation.php; fall back to English label when unavailable
        const text = typeof translate === 'function' ? translate(label) : label;
        badge.appendChild(document.createTextNode(text));
        return badge;
    }

    /**
     * Render all PPS signals (status + detectors + PubPeer links) as inline badges.
     * @param {Object} citation - Formatted citation object
     * @returns {DocumentFragment}
     */
    renderSuspectBadges(citation) {
        const fragment = document.createDocumentFragment();

        // Status badges (exclude 'Genuine' which has its own badge)
        (citation.status || []).filter(s => s !== 'Genuine').forEach(s => {
            const entry = BiblioRefParser.STATUS_LABELS[s];
            fragment.appendChild(document.createTextNode(' '));
            fragment.appendChild(
                this._makeBadge(
                    entry ? entry.label : s,
                    entry ? entry.color : 'default',
                    entry ? entry.desc  : undefined
                )
            );
        });

        // Detector badges (icon is decorative, aria-hidden by _makeIcon)
        (citation.detectors || []).forEach(key => {
            const entry = BiblioRefParser.DETECTOR_LABELS[key];
            fragment.appendChild(document.createTextNode(' '));
            fragment.appendChild(
                this._makeBadge(
                    entry ? entry.label : key,
                    entry ? entry.color : 'info',
                    entry ? entry.desc  : undefined,
                    'fa-solid fa-triangle-exclamation'
                )
            );
        });

        // PubPeer links (icon-only, labelled for screen readers)
        (citation.pubpeerurl || []).forEach(url => {
            if (!/^https?:\/\/[^\s<>"]+$/.test(url)) {
                return;
            }
            const link = document.createElement('a');
            link.href = url;
            link.rel = 'noopener noreferrer';
            link.target = '_blank';
            link.className = 'biblio-ref-pubpeer-link';
            link.setAttribute('aria-label', typeof translate === 'function' ? translate('View on PubPeer') : 'View on PubPeer');
            link.title = typeof translate === 'function' ? translate('More information') : 'More information';
            link.appendChild(this._makeIcon('fa-solid fa-circle-info'));
            fragment.appendChild(document.createTextNode(' '));
            fragment.appendChild(link);
        });

        return fragment;
    }

    /**
     * Render the Genuine badge (Bootstrap label-success).
     * @returns {HTMLElement}
     */
    renderGenuineBadge() {
        const entry = BiblioRefParser.STATUS_LABELS['Genuine'];
        const badge = this._makeBadge('Genuine', 'success', entry ? entry.desc : undefined);
        return badge;
    }

    /**
     * Render error message
     * @param {string} message - Error message
     * @returns {HTMLLIElement} List item with error
     */
    renderError(message) {
        const li = document.createElement('li');
        li.textContent = message;
        li.className = 'biblio-ref-error';
        return li;
    }

    /**
     * Render all citations
     * @param {Array} citations - Array of formatted citations
     */
    renderCitations(citations) {
        const fragment = document.createDocumentFragment();

        citations.forEach(citation => {
            fragment.appendChild(this.renderCitation(citation));
        });

        this.container.textContent = '';
        this.container.appendChild(fragment);

        const publicLegend = document.getElementById('biblio-ref-legend-public');
        if (publicLegend && citations.some(c => c.isSuspect)) {
            publicLegend.removeAttribute('hidden');
        }
    }

    /**
     * Clear container
     */
    clear() {
        this.container.textContent = '';
    }

    /**
     * Escape HTML to prevent XSS
     * @param {string} str - String to escape
     * @returns {string} Escaped string
     */
    escapeHtml(str) {
        if (!str) return '';
        const div = document.createElement('div');
        div.textContent = str;
        return div.innerHTML;
    }
}

/**
 * Main manager class
 */
class BiblioRefManager {
    /**
     * Create a new manager
     * @param {Object} config - Configuration object
     * @param {string} config.containerSelector - Selector for biblio refs container
     * @param {string} config.triggerSelector - Selector for visualization trigger element
     * @param {string} config.sectionSelector - Selector for the section to show/hide
     */
    constructor(config = {}) {
        this.config = {
            containerSelector: '#biblio-refs-container',
            triggerSelector: '#visualize-biblio-refs',
            sectionSelector: '#biblio-refs',
            ...config,
        };

        this.service = new BiblioRefService();
        this.initialized = false;
    }

    /**
     * Get configuration from DOM element data attributes
     * @param {HTMLElement} element - Trigger element
     * @returns {Object} Configuration object
     */
    getConfigFromElement(element) {
        return {
            apiUrl: element.dataset.api,
            paperUrl: element.dataset.value,
            showAll: parseInt(element.dataset.all, 10) === 1,
        };
    }

    /**
     * Initialize and visualize bibliographic references
     */
    async initialize() {
        // Prevent multiple initializations
        if (this.initialized) {
            return;
        }

        const triggerElement = document.querySelector(
            this.config.triggerSelector
        );
        if (!triggerElement) {
            return; // Element not present on this page
        }

        const container = document.querySelector(this.config.containerSelector);
        if (!container) {
            console.error('Biblio refs container not found');
            return;
        }

        const section = document.querySelector(this.config.sectionSelector);
        const renderer = new BiblioRefRenderer(container);

        try {
            // Mark as initialized to prevent duplicate calls
            this.initialized = true;

            // Get configuration from DOM
            const config = this.getConfigFromElement(triggerElement);

            // Fetch citations
            const response = await this.service.fetchCitations(
                config.apiUrl,
                config.paperUrl,
                config.showAll
            );

            // Handle empty or message-only response
            if (response.message && Object.keys(response).length === 1) {
                // API returned only a message, no citations to display
                return;
            }

            // Parse citations
            const citations = Object.values(response)
                .map(citation =>
                    BiblioRefParser.parseCitation(citation, config.showAll)
                )
                .filter(citation => citation !== null);

            // Render citations if we have any
            if (citations.length > 0) {
                renderer.renderCitations(citations);

                // Show section if available
                if (section) {
                    section.style.display = 'block';
                }
            }
        } catch (error) {
            console.error('Failed to visualize biblio refs:', error);
            renderer.clear();
            renderer.container.appendChild(
                renderer.renderError(
                    error.message || 'Failed to load citations'
                )
            );
        }
    }
}

/**
 * Initialize on DOM ready
 */
if (typeof document !== 'undefined') {
    // jQuery ready handler for backward compatibility
    if (typeof $ !== 'undefined' && typeof $.fn !== 'undefined') {
        $(function () {
            const manager = new BiblioRefManager();
            manager.initialize();
        });
    } else {
        // Vanilla JS fallback
        if (document.readyState === 'loading') {
            document.addEventListener('DOMContentLoaded', () => {
                const manager = new BiblioRefManager();
                manager.initialize();
            });
        } else {
            // DOM already loaded
            const manager = new BiblioRefManager();
            manager.initialize();
        }
    }
}

// Export for testing
if (typeof module !== 'undefined' && module.exports) {
    module.exports = {
        BiblioRefService,
        BiblioRefParser,
        BiblioRefRenderer,
        BiblioRefManager,
    };
}
